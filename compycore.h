typedef struct Compycore
{
	bool introduced = false;
	bool winked = false;
	int michaelX = 29;
	int michaelY = -46;
	int michaelTargetY = 5;
	int logoX = 30, logoY = 46;

	bool introduce()
	{
		if (michaelY < michaelTargetY)
		{
			michaelY++;
			sprites.drawSelfMasked(michaelX, michaelY, michael, 0);
		}
		else
		{
			if (introduced | arduboy.everyXFrames(150))
			{
				introduced = true;
				return true;
			}

			michaelY = michaelTargetY;

			if (!winked)
			{
				sound.tone(1046, 50, 2093, 100);
				winked = true;
			}

			sprites.drawSelfMasked(michaelX, michaelY, wink, 0);
			sprites.drawPlusMask(logoX, logoY, logo, 0);
		}

		arduboy.display();
		return false;
	}
} Compycore;
