void prettyPrintNumber(String number, int x, int y)
{
	int numberWidth = 7;

	for (int i = 0; i < number.length(); i++)
	{
		sprites.drawPlusMask(x, y, numbers, int(number.charAt(i)) - 48);
		x += numberWidth;
	}
}

void fillRectPattern(int x, int y, int w, int h)
{

	for (int tempY = y; tempY < y + h; tempY++)
	{
		// select the starting color
		int color = tempY % 2;

		for (int tempX = x; tempX < x + w; tempX++)
		{
			arduboy.drawPixel(tempX, tempY, color);

			// swap the color
			color = 1 - color;
		}
	}
}
